@pytest.mark.asyncio
async def test_autogen_writes_hash_and_prints_password_block(tmp_path: Path) -> None:
    """Fresh boot with no env var: seed writes a real hash and stdout
    carries the bracketed password block."""
    settings = build_settings(tmp_path)  # admin_password defaults to None
    engine = create_engine(settings)
    stdout_buf = io.StringIO()
    stderr_buf = io.StringIO()
    try:
        with patch("sys.stdout", new=stdout_buf), patch("sys.stderr", new=stderr_buf):
            result = await initialize_schema(engine, settings)
        assert result.fresh is True
        assert result.password_source == "autogen"

        password = _captured_autogen_password(stdout_buf)
        assert len(password) == 20

        sm = make_sessionmaker(engine)
        async with sm() as s:
            user = (await s.execute(select(UserORM))).scalar_one()
            settings_row = (await s.execute(select(SettingsORM))).scalar_one()
        assert user.password_hash is not None
        assert verify_password(user.password_hash, password).ok is True
        assert settings_row.first_run_complete == 1

        # Sentinel + reset hint must be present so the operator does not
        # miss the credential delivery in a noisy log stream.
        out = stdout_buf.getvalue()
        assert "RESTREAM_PLUS FIRST BOOT" in out
        assert "Save this password now" in out
        assert (
            "================================================================================"
            in out
        )
        assert "RESTREAM_RESET_ADMIN_PASSWORD=1" in out

        # The credential block must NOT leak to stderr where structlog
        # routes its JSON output.
        assert password not in stderr_buf.getvalue()
    finally:
        await engine.dispose()


