def _captured_autogen_password(stdout: io.StringIO) -> str:
    """Extract the autogen password from the captured stdout block.

    The block format is defined by ``_print_autogen_password`` in
    ``app/db/schema_init.py``. The password line looks like::

        "    Password : <20-char value>"
    """
    for line in stdout.getvalue().splitlines():
        stripped = line.strip()
        if stripped.startswith("Password :"):
            return stripped.split(":", 1)[1].strip()
    raise AssertionError(
        f"no autogenerated password found in stdout; captured:\n{stdout.getvalue()!r}"
    )


